import Foundation

/// ELEVATE UI Framework
///
/// A native iOS UI component library based on the ELEVATE Design System.
/// This framework provides SwiftUI and UIKit components styled with ELEVATE design tokens.
///
/// ## Topics
///
/// ### Design Tokens
/// - ``ElevateTypography``
/// - ``ElevateIconSize``
/// - ``ButtonTokens``
/// - ``ChipTokens``
/// - ``BadgeTokens``
///
/// ### SwiftUI Components
/// - ``ElevateButton``
///
/// ### UIKit Components
/// - ``ElevateButton``
public struct ElevateUI {
    /// The current version of the ELEVATE UI framework
    public static let version = "0.1.0"

    /// The ELEVATE Design System version this framework is based on
    public static let designSystemVersion = "0.36.1"
}
